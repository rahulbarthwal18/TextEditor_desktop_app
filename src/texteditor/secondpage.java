    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // open file option
         FileDialog filedialog = new FileDialog(secondpage.this,"Open file",FileDialog.LOAD);
        filedialog.setVisible(true);
        if(filedialog.getFile()!=null){
            untitled = filedialog.getDirectory() + filedialog.getFile();
            setTitle(untitled);
        }
        try{
             try (BufferedReader reader = new BufferedReader(new FileReader(untitled))) {
                 StringBuilder sb= new StringBuilder();
                 String line = null;
                 while((line=reader.readLine())!=null){
                     StringBuilder append = sb.append(line).append("\n");
                     jTextArea1.setText(sb.toString());
                 }}
        } catch (IOException e) {
        System.out.println("file not found");
    }
       
    }//GEN-LAST:event_jMenuItem2ActionPerformed


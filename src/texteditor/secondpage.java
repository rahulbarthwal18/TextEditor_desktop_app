    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        // save file option
         FileDialog filedialog = new FileDialog(secondpage.this,"Save file",FileDialog.SAVE);
        filedialog.setVisible(true);
        if(filedialog.getFile()!=null){
            untitled = filedialog.getDirectory() + filedialog.getFile();
            setTitle(untitled);
        }
        try{
             try (FileWriter filewriter = new FileWriter(untitled)) {
                 
                 filewriter.write(jTextArea1.getText());
                 setTitle(untitled);
                 filewriter.close();
             }
        } catch (IOException e) {
        System.out.println("file not found");
    }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

